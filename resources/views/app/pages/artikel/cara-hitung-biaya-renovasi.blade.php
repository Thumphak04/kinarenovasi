<script>
function hitungEstimasi() {
    const luas = document.getElementById('luasRumah').value;
    const hargaPerM2 = document.getElementById('jenisRenovasi').value;
    const hasil = document.getElementById('hasilEstimasi');
    
    if (luas && luas > 0) {
        const total = luas * hargaPerM2;
        const formatted = new Intl.NumberFormat('id-ID', {
            style: 'currency',
            currency: 'IDR',
            minimumFractionDigits: 0
        }).format(total);
        
        hasil.innerHTML = `Estimasi: ${formatted}`;
        hasil.style.display = 'block';
    } else {
        alert('Masukkan luas rumah yang valid');
    }
}

// FAQ Toggle Function
document.addEventListener('DOMContentLoaded', function() {
    const faqQuestions = document.querySelectorAll('.faq-question');
    
    faqQuestions.forEach(question => {
        question.addEventListener('click', function() {
            const faqItem = this.parentElement;
            const isActive = faqItem.classList.contains('active');
            
            // Close all FAQ items
            document.querySelectorAll('.faq-item').forEach(item => {
                item.classList.remove('active');
            });
            
            // Open clicked item if it wasn't active
            if (!isActive) {
                faqItem.classList.add('active');
            }
        });
    });
});
</script>
